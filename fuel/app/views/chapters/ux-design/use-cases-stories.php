                    <h2>Use Cases</h2>
                    <p>Use cases are a string of steps a user takes to perform an action. Take the goals of the project and define how users achieve those goals.</p>
                    <blockquote class="example example-sample">
                    	<p>Buying a product from Amazon</p>
                    	<ul>
                    		<li>Search for a product or use categories to browse</li>
                    		<li>View product information</li>
                    		<li>Add product to basket</li>
                    		<li>Checkout basket</li>
                    		<li>Receive confirmation of purchase</li>
                    	</ul>
                    </blockquote>
                    <h2>User Stories</h2>
                    <p>Describe the actions for each type of user that would use the project.</p>
                    <blockquote class="example example-sample">
                    	<p>As a frequent visitor, I want to be able to stay signed in for 30 days.</p>
                    </blockquote>
                    <blockquote class="example example-sample">
                    	<p>As a new visitor, I want to be sign up to create a wish list.</p>
                    </blockquote>
