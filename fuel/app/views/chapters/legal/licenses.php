				<p>A license for each library, framework or software included with the application being built must be recorded. Place each license as a text file in a ‘Licenses’ folder in ‘Admin’.</p>
				<p>The format for each license is as follows:<br />
					[Software] [Software version] [Licence + version].txt
				</p>
				<blockquote class="example example-sample">
					<p>
						PHP 5.4 The PHP License 3.01.txt<br />
						jQuery 1.9.1 MIT License.txt
					</p>
				</blockquote>
				<p>Remember to take note of the software version and the license number, if applicable.</p>
				<p>If two pieces of software use the same license (for example, MIT) you must include one for each piece of software in full.</p>
