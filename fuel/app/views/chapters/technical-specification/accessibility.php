				<p>Accessibility is a complicated issue and should go beyond box-ticking.</p>
				<p>We recommend reading <a href="http://www.w3.org/TR/UNDERSTANDING-WCAG20/">Understanding WCAG 2.0, <em>A guide to understanding and implementing Web Content Accessibility Guidelines 2.0</em></a> as an introduction, then use <a href="http://www.w3.org/TR/WCAG20/">Web Content Accessibility Guidelines (WCAG) 2.0</a> for reference.</p>
				<p>However, that can take some time, so here’s a quick overview of the four main options.</p>
				<table class="datatable datatable-blocks">
					<thead>
						<tr>
							<th>Conformance</th>
							<th>Description</th>
						</tr>
					</thead>
					<tbody>
						<tr>
							<th>None</th>
							<td>No regard for accessibility. Should only be considered for internal applications or administration systems for small businesses.</td>
						</tr>
						<tr>
							<th>A</th>
							<td>Including basic accessibility functions such as text alternatives to visuals, allowing the site to work without JavaScript, making sure that elements on the page used for decoration are not picked up by screen readers, help with CAPTCHA, captions for pre-recorded video or audio, use of colour (do not use to convey information that cannot otherwise be explained), keyboard usage, content is ordered appropriately, titles are accurate and useful, labels for forms.</td>
						</tr>
						<tr>
							<th>AA</th>
							<td>All of A plus: captions for live video or audio, minimum contrast, text resize by 200% without loss of functionality, headings and labels describe a purpose, always show focus, navigation consistent, error prevention (including reviewing for submitting).</td>
						</tr>
						<tr>
							<th>AAA</th>
							<td>All of A &amp; AA plus: enhanced contrast, audio has no background noise, <a href="http://www.w3.org/TR/UNDERSTANDING-WCAG20/visual-audio-contrast-visual-presentation.html">text is highly readable</a>, no interruptions of timing restraints, no loss of data if a session times out, <a href="http://www.w3.org/TR/2012/NOTE-WCAG20-TECHS-20120103/G19">three flashes rule</a>, ‘breadcrumb’ navigation, identify jargon and abbreviations, context-aware help on each page.</td>
						</tr>
					</tbody>
				</table>
				<p>You can use the <a href="http://www.w3.org/WAI/WCAG20/quickref/">How to Meet WCAG 2.0</a> tool to discover what level of conformance you can achieve.</p>
				<p>Making sites accessible doesn’t just help an impaired visitor, it can also force you to write bug-free applications, and even if the highest levels of conformance are not required, doing so is good practice and what Architect strives to achieve.</p>
